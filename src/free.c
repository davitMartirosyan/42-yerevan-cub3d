#include "cube.h"

void	free_char_pp(char ***pp)
{
	int	i;

	i = -1;
	if (!(*pp) && !*(*pp))
		return ;
	while ((*pp)[++i])
		free((*pp)[i]);
	free((*pp));
	(*pp) = NULL;
}